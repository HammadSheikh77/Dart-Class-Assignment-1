// Q.2: Take two variables and store age then using if/else condition
// to determine oldest and youngest among them.

void main() {
  //QUESTION NO:2

  int age1 = 22;
  int age2 = 20;

  if (age1 < age2) {
    //Starting of if-else
    print("AGE 1 IS YOUNGEST");
  } else if (age1 == age2) {
    print("BOTH ARE SAME AGE");
  } else {
    print("AGE 2 IS YOUNGEST");
  } //Ending of if-else
}
