void main() {
  //QUESTION NO:6
  String alphabet = "u";
  if (alphabet == "a" ||
      alphabet == "e" ||
      alphabet == "i" ||
      alphabet == "o" ||
      alphabet == "u") {
    //Starting of if-else
    print("Alphabet Is Vowel");
  } else {
    print("Alphabet Is Constant");
  } //Ending of if-else
}
